#include "libft.h"

void ft_bzero(void *s, size_t n)
{
  int i;
  unsigned char *s_ucp;

  i = -1;
  s_ucp = (unsigned char *)s;
  while(++i < n)
  {
      s_ucp[i] = '\0';
  }
}
